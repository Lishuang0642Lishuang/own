/**
 * @desc: 测试i++ 和 ++i
 * @author: 英布
 * @date: 2023/8/18
 *
 */
public class TestPlus {


    public static void main(String[] args) {
        for (int i = 0; i < 10 ;i++){
            System.out.println("i:" + i);
        }

        for (int j = 0; j < 10; ++j) {
            System.out.println("j:" + j);
        }



    }
}
